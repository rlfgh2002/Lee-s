class User
{
  String UID = "";
  String fullName = "";
  String avatar = "";
  String caption = "";

  User({
    String UID = "",
    String fullName = "",
    String avatar = "",
    String caption = "",
  }){
    this.UID = UID;
    this.fullName = fullName;
    this.avatar = avatar;
    this.caption = caption;
  }
}