class User {
  String UID = "";
  String fullName = "";
  String avatar = "";
  String caption = "";
  String gisu = "";

  User({
    String UID = "",
    String fullName = "",
    String avatar = "",
    String caption = "",
    String gisu = "",
  }) {
    this.UID = UID;
    this.fullName = fullName;
    this.avatar = avatar;
    this.caption = caption;
    this.gisu = gisu;
  }
}
