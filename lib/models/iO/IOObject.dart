  String idx = "";
  String name = "";
  String company = "";
  String shortContent = "";
  String content = "";
  String listImgUrl = "";
  String viewImgUrl_1 = "";
  String viewImgUrl_2 = "";

  IOObject({
    String idx,
    String name,
    String company,
    String shortContent,
    String content,
    String listImgUrl,
    String viewImgUrl_1,
    String viewImgUrl_2,
  }) {
    this.idx = idx;
    this.name = name;
    this.company = company;
    this.shortContent = shortContent;
    this.content = content;
    this.listImgUrl = listImgUrl;
    this.viewImgUrl_1 = viewImgUrl_1;
    this.viewImgUrl_2 = viewImgUrl_2;
  }
}
