class MapObject {
  String companyCode = "";
  String companyName = "";
  String companyTel = "";
  String companyAddress = "";

  MapObject({
    String companyCode,
    String companyName,
    String companyTel,
    String companyAddress,
  }) {
    this.companyCode = companyCode;
    this.companyName = companyName;
    this.companyTel = companyTel;
    this.companyAddress = companyAddress;
  }
}
